
    create table bicycles (
        brake_type_id integer not null,
        gear_count integer not null,
        is_foldable boolean not null,
        suspension_type_id integer not null,
        id bigint generated by default as identity,
        vehicle_id bigint not null unique,
        primary key (id)
    );

    create table brake_types (
        id integer generated by default as identity,
        brake varchar(50) not null unique,
        primary key (id)
    );

    create table cars (
        displacement_cc integer not null,
        door_count integer not null,
        license_plate varchar(7) not null unique,
        id bigint generated by default as identity,
        vehicle_id bigint not null unique,
        primary key (id)
    );

    create table category (
        id integer generated by default as identity,
        category varchar(50) not null unique,
        primary key (id)
    );

    create table fuel_types (
        id integer generated by default as identity,
        fuel varchar(50) not null unique,
        primary key (id)
    );

    create table motorcycles (
        displacement_cc integer not null,
        license_plate varchar(7) not null unique,
        id bigint generated by default as identity,
        vehicle_id bigint not null unique,
        primary key (id)
    );

    create table suspension_types (
        id integer generated by default as identity,
        suspension varchar(50) not null unique,
        primary key (id)
    );

    create table vehicle_types (
        id integer generated by default as identity,
        vehicle varchar(50) not null unique,
        primary key (id)
    );

    create table vehicles (
        category_id integer not null,
        fuel_type_id integer not null,
        production_year integer not null,
        vehicle_type_id integer not null,
        wheel_count integer not null,
        id bigint generated by default as identity,
        brand varchar(50) not null,
        color varchar(50) not null,
        model varchar(50) not null,
        primary key (id)
    );

    alter table if exists bicycles 
       add constraint fk_vehicle_braketype 
       foreign key (brake_type_id) 
       references brake_types;

    alter table if exists bicycles 
       add constraint fk_vehicle_suspensiontype 
       foreign key (suspension_type_id) 
       references suspension_types;

    alter table if exists bicycles 
       add constraint fk_vehicle_bicycle 
       foreign key (vehicle_id) 
       references vehicles;

    alter table if exists cars 
       add constraint fk_vehicle_car 
       foreign key (vehicle_id) 
       references vehicles;

    alter table if exists motorcycles 
       add constraint fk_vehicle_motorcycle 
       foreign key (vehicle_id) 
       references vehicles;

    alter table if exists vehicles 
       add constraint fk_vehicle_category 
       foreign key (category_id) 
       references category;

    alter table if exists vehicles 
       add constraint fk_vehicle_fueltype 
       foreign key (fuel_type_id) 
       references fuel_types;

    alter table if exists vehicles 
       add constraint fk_vehicle_vehicletype 
       foreign key (vehicle_type_id) 
       references vehicle_types;
